async function main() {
  for (const post of POSTS) {
    const page = await prisma.page.findUnique({ where: { slug: post.pageSlug } });
    if (!page) {
      console.warn(`✗ page not found for slug ${post.pageSlug}, skipping ${post.slug}`);
      continue;
    }

    await prisma.post.upsert({
      where: { slug: post.slug },
      update: { pageId: page.id },
      create: {
        title: post.title,
        slug: post.slug,
        excerpt: post.excerpt,
        status: 'PUBLISHED',
        category: 'OTHER',
        pageId: page.id,
        publishedAt: new Date(),
        blocks: post.blocks,
      },
    });
    console.log(`✓ ${post.slug} → ${post.pageSlug}`);
  }
}

main()
  .then(() => prisma.$disconnect())
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
